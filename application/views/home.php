                                                <a style="color:black;font-size: 1rem;" class="rsswidget"
                                                    href="#"><b style="color:#5a1188;font-size: 1rem;">SU to take Pre-Entry Test from 8960 candidates of 14 districts, 4 provinces out of total 19045 aspirants in 1st phase for admission to bachelor programs.</b>
</br>
JAMSHORO: The pre-entry test for admissions to bachelor degree programs for the academic year 2023 will be held on October 30 in the first phase at the University of Sindh Jamshoro, wherein a total of 8960 candidates including 7360 male and 1600 female candidates will participate.

According to the Director Admissions Professor Dr. Ayaz Keerio, as many as 8960 candidates out of a total of 19045 candidates including 4488 girls are being called for the entry test in the first phase.

He said those candidates who had been called to appear in pre-entrance test belonged to 14 districts of Sindh including Dadu, Larkana, Qambar-Shahdadkot, Ghotki, Shikarpur, Jacobabad, Kashmore-Kandhkot, Sukkur, Khairpur Mir’s, Naushero Feroze, Shaheed Benazirabad, Sanghar, Karachi and Umerkot.

He further stated that the candidates of the Punjab, Balochistan, Khyber Pakhtunkhwa and Gilgit-Baltistan provinces will also take the test on October 30.

He said that the admit cards will be sent to the candidates of the above-mentioned districts tomorrow (Friday) through their e-portals and the e-mail IDs provided on the admission form.

According to Dr. Keerio, out of the candidates of the above 14 districts, if some of them did not receive their admit cards through the e-portal and their given email IDs by Friday, they will be called to appear in the entrance test in the second phase of the test which is scheduled to be held on November 6.

It may be noted that the Pre-Entry Test is being conducted in two phases only, while the aspirants of the bachelor degree programmes could take part in it.<br>
